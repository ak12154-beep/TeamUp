export default function LegalDocument({ type = 'terms' }) {
 const { t } = useTranslation()
 const navigate = useNavigate()
 const location = useLocation()
 const title = type === 'privacy' ? t('legal.privacyTitle') : t('legal.termsTitle')
 const subtitle = type === 'privacy' ? t('legal.privacySubtitle') : t('legal.termsSubtitle')

 const close = () => {
  if (location.key !== 'default') {
   navigate(-1)
   return
  }
  navigate('/register')
 }

 return (
  <div className="auth-page legal-page">
   <div className="auth-container legal-container">
    <div className="legal-header">
     <Link to="/" className="auth-logo legal-logo">
      <img src={logoSrc} alt="TeamUp" />
      <span>TeamUp</span>
     </Link>
     <button type="button" className="legal-close" onClick={close} aria-label={t('legal.close')}>
      ×
     </button>
    </div>

    <div className="legal-content">
     <h1>{title}</h1>
     <p className="auth-subtitle">{subtitle}</p>

     <div className="legal-card">
      {type === 'terms' ? (
       TERMS_SECTIONS.map((section) => (
        <section key={section.title} className="legal-section">
         <h2>{section.title}</h2>
         {section.paragraphs.map((paragraph) => (
          <p key={paragraph}>{paragraph}</p>
         ))}
         {section.list.length ? (
          <ul className="legal-list">
           {section.list.map((item) => (
            <li key={item}>{item}</li>
           ))}
          </ul>
         ) : null}
        </section>
       ))
      ) : (
       <section className="legal-section">
        {PRIVACY_PARAGRAPHS.map((paragraph) => (
         <p key={paragraph}>{paragraph}</p>
        ))}
       </section>
      )}
     </div>
    </div>
   </div>
  </div>
 )
}
